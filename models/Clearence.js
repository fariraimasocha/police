import mongoose from "mongoose";
import { refreshModel } from "@/utils/refreshModel";
const clearenceSchema = new mongoose.Schema(
  {
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    idNumber: {
      type: String,
      required: true,
    },
    imageUrl: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const Clearence = refreshModel("Clearence", clearenceSchema);
export default Clearence;
